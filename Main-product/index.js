function productList(){
    const product_item_container = document.getElementById('product-item-container');
    product.forEach(myfunc);
    function myfunc(value){
       const product_item = document.createElement('div'); 
       product_item.classList.add("product-item");
       const main_product = document.createElement('div');
       main_product.classList.add("main-product")
     
       main_product.innerHTML = ` <img class="productImage" src="${value.product_img}" alt="${product.name}">
       <h3 class="productname">${value.product_name}</h3>
       <h6><span>₹</span> ${value.price}</h6>
       `
       
       product_item.addEventListener('click', () => single_product(value));
    //    document.getElementById('product-item-container').appendChild(product_item);
   
    product_item_container.appendChild(product_item);
    product_item.appendChild(main_product);
    
   
   };
    }

function single_product(product){
   
    const single_product = document.getElementById('single-product');
    const product_item_container = document.getElementById('product-item-container');
    const product_content = document.getElementById('product-content');
    const singleProductDetails = document.getElementById('singleProductDetails');
    const product_images = document.getElementById('product-images');
    const variantGalleryDiv = document.getElementById('variantGallery');
    
    const mainProductImage = document.getElementById('mainProductImage');
    const thumbImage = document.getElementById('thumb-img');
    
    singleProductDetails.innerHTML = `
    <h3 class="productname">${product.product_name}</h3>
            <p>${product.Description}</p>
            <p><b>Category:</b> ${product.category}</p>
            <p><b>Seller:</b> ${product.seller}</p>
            <h6> <span>₹</span> ${product.price}</h6>
`

mainProductImage.src = product.product_img;

            variantGalleryDiv.innerHTML = '';
      product.alternate_img.forEach(variant => {
        const variantImage = document.createElement('img');
        variantImage.src = variant.image;
        variantImage.classList.add('variantImage');
        variantImage.addEventListener('click', () => changeProductImage(variant.image));
        variantGalleryDiv.appendChild(variantImage);
      });
     
    thumbImage.src = product.alternate_img[0].image;

single_product.appendChild(product_content);
single_product.appendChild(product_images);
product_content.appendChild(singleProductDetails);
product_images.appendChild(mainProductImage);
product_images.appendChild(variantGalleryDiv);

    product_item_container.style.display = 'none';
    single_product.style.display = 'block';


}

function changeProductImage(imageUrl) {
    const mainProductImage = document.getElementById('mainProductImage');
    mainProductImage.src = imageUrl;
  }

    

